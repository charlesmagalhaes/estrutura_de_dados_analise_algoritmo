
public class ArrayEscrita {
	int valor;
	boolean modificado = false;
	
	public ArrayEscrita() {
	}
	
	public int getValor() {
		return valor;
	}
	public void setValor(int valor) {
		this.valor = valor;
	}

	public boolean getModificado() {
		return modificado;
	}

	public void setModificado(boolean modificado) {
		this.modificado = modificado;
	}

	
	
	

}
